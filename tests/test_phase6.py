class TestLambdaHandler:
    def _invoke(self, module: str, method: str, path: str, body: str | None = None) -> dict:
        """Helper: load handler.py fresh with a given MCP_SERVER_MODULE and invoke it."""
        import importlib.util
        spec = importlib.util.spec_from_file_location(
            "handler_mod", ROOT / "aws" / "lambda" / "handler.py"
        )
        handler_mod = importlib.util.module_from_spec(spec)
        spec.loader.exec_module(handler_mod)

        event = {
            "version": "2.0",
            "routeKey": f"{method} {path}",
            "rawPath": path,
            "rawQueryString": "",
            "headers": {"host": "localhost", "content-type": "application/json"},
            "requestContext": {
                "http": {"method": method, "path": path, "sourceIp": "127.0.0.1"},
                "stage": "$default",
            },
            "isBase64Encoded": False,
        }
        if body is not None:
            event["body"] = body

        class FakeContext:
            function_name = "test"
            memory_limit_in_mb = 128
            aws_request_id = "test-request-id"

        return handler_mod.handler(event, FakeContext())

    def test_cmms_health_check(self, monkeypatch, tmp_path):
        monkeypatch.setenv("MCP_SERVER_MODULE", "mcp_servers.cmms_mcp:app")
        monkeypatch.setenv("CMMS_DB_PATH", str(tmp_path / "cmms.db"))
        response = self._invoke("mcp_servers.cmms_mcp:app", "GET", "/health")
        assert response["statusCode"] == 200
        assert "cmms-mcp" in response["body"]

    def test_cmms_create_work_order_via_lambda(self, monkeypatch, tmp_path):
        """
        Regression test: with lifespan="off", FastAPI's lifespan context
        manager never fires, so init_db() must be called explicitly by
        handler.py itself — otherwise this fails with
        'no such table: work_orders' on the very first request.
        """
        monkeypatch.setenv("MCP_SERVER_MODULE", "mcp_servers.cmms_mcp:app")
        monkeypatch.setenv("CMMS_DB_PATH", str(tmp_path / "cmms.db"))
        body = json.dumps({
            "machine_id": "PUMP-01", "fault_type": "bearing_wear",
            "description": "test", "priority": "high",
        })
        response = self._invoke("mcp_servers.cmms_mcp:app", "POST", "/work_orders", body)
        assert response["statusCode"] == 200
        parsed = json.loads(response["body"])
        assert parsed["status"] == "open"

    def test_inventory_health_check(self, monkeypatch, tmp_path):
        monkeypatch.setenv("MCP_SERVER_MODULE", "mcp_servers.inventory_mcp:app")
        monkeypatch.setenv("INVENTORY_DB_PATH", str(tmp_path / "inventory.db"))
        response = self._invoke("mcp_servers.inventory_mcp:app", "GET", "/health")
        assert response["statusCode"] == 200

    def test_inventory_parts_seeded_via_lambda(self, monkeypatch, tmp_path):
        """Same init_db() concern as cmms — inventory's seed data must appear on first request."""
        monkeypatch.setenv("MCP_SERVER_MODULE", "mcp_servers.inventory_mcp:app")
        monkeypatch.setenv("INVENTORY_DB_PATH", str(tmp_path / "inventory.db"))
        response = self._invoke("mcp_servers.inventory_mcp:app", "GET", "/parts")
        assert response["statusCode"] == 200
        assert len(json.loads(response["body"])) == 8

    def test_scheduling_health_check(self, monkeypatch, tmp_path):
        monkeypatch.setenv("MCP_SERVER_MODULE", "mcp_servers.scheduling_mcp:app")
        monkeypatch.setenv("SCHEDULING_DB_PATH", str(tmp_path / "scheduling.db"))
        response = self._invoke("mcp_servers.scheduling_mcp:app", "GET", "/health")
        assert response["statusCode"] == 200

    def test_notify_health_check(self, monkeypatch, tmp_path):
        """notify-mcp has no init_db() at all — handler.py's hasattr guard must not break on this."""
        monkeypatch.setenv("MCP_SERVER_MODULE", "mcp_servers.notify_mcp:app")
        monkeypatch.setenv("NOTIFY_LOG_PATH", str(tmp_path / "alerts.log"))
        response = self._invoke("mcp_servers.notify_mcp:app", "GET", "/health")
        assert response["statusCode"] == 200


