class TestEventBridgePackaging:
    def test_orchestrator_package_contains_only_agent_and_data(self, tmp_path, monkeypatch):
        import importlib.util
        spec = importlib.util.spec_from_file_location(
            "eb_deploy_mod", ROOT / "aws" / "eventbridge" / "deploy.py"
        )
        eb_deploy_mod = importlib.util.module_from_spec(spec)
        spec.loader.exec_module(eb_deploy_mod)

        # Only redirect where the staging dir + output zip get built —
        # EVENTBRIDGE_DIR must stay pointing at the real aws/eventbridge/
        # folder, since that's where scheduler_handler.py (the file being
        # packaged) actually lives. build_orchestrator_package() always
        # reads FROM EVENTBRIDGE_DIR and the project ROOT, regardless of
        # where staging happens — so we only need to redirect the BUILD_DIR
        # check (kept absent here, deliberately, to exercise the
        # no-dependencies warning path) and the final output path.
        monkeypatch.setattr(eb_deploy_mod, "ORCHESTRATOR_PACKAGE", tmp_path / "orchestrator_package.zip")

        out_path = eb_deploy_mod.build_orchestrator_package()

        with zipfile.ZipFile(out_path) as zf:
            names = zf.namelist()

        assert "scheduler_handler.py" in names
        assert "agent/react_agent.py" in names
        assert "data/store.py" in names

        # Regression check: models/ and mcp_servers/ were bundled
        # unnecessarily in an early draft — neither is actually imported
        # by the orchestrator's real code path (verified empirically
        # during development by tracking sys.modules before/after the
        # actual import statements).
        assert not any(n.startswith("models/") for n in names)
        assert not any(n.startswith("mcp_servers/") for n in names)


