class TestDockerComposeVolumes:
    @pytest.fixture
    def compose_text(self):
        return (ROOT / "docker-compose.yml").read_text()

    def test_no_volume_mounted_directly_over_mcp_servers_code_dir(self, compose_text):
        """
        Regression test: mounting a named volume at /app/mcp_servers
        would shadow the application code COPY'd there at build time,
        causing every lightweight server to fail to import on startup.
        Checks specifically for that as a VOLUME MOUNT TARGET (the
        ":/app/mcp_servers" pattern docker-compose uses), not just any
        mention of "mcp_servers" — the file's own comments legitimately
        reference mcp_servers/ by name elsewhere.
        """
        assert ":/app/mcp_servers" not in compose_text

    def test_volumes_mounted_at_dedicated_data_dir(self, compose_text):
        assert "/app/data" in compose_text

    def test_each_lightweight_service_sets_its_db_path_env_var(self, compose_text):
        for env_var in ["CMMS_DB_PATH", "INVENTORY_DB_PATH", "SCHEDULING_DB_PATH", "NOTIFY_LOG_PATH"]:
            assert env_var in compose_text


