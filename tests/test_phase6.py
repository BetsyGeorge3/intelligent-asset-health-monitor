class TestDockerignore:
    @pytest.fixture
    def patterns(self):
        with open(ROOT / ".dockerignore") as f:
            return [line.strip() for line in f if line.strip() and not line.startswith("#")]

    def _is_excluded(self, path: str, patterns: list[str]) -> bool:
        return any(fnmatch.fnmatch(path, p) for p in patterns)

    def test_sensors_db_is_NOT_excluded(self, patterns):
        """
        Regression test: data/sensors.db must be INCLUDED in the Docker
        build context, since Dockerfile.sensor's `COPY data/ ./data/`
        depends on it existing at build time. A bare `*.db` pattern
        would silently break this.
        """
        assert not self._is_excluded("data/sensors.db", patterns)

    def test_mcp_server_databases_ARE_excluded(self, patterns):
        """These are runtime-generated by each server's init_db() — never needed at build time."""
        for path in ["mcp_servers/cmms.db", "mcp_servers/inventory.db",
                     "mcp_servers/scheduling.db", "mcp_servers/alerts.log"]:
            assert self._is_excluded(path, patterns), f"{path} should be excluded but isn't"

    def test_sensors_db_wal_shm_sidecars_excluded(self, patterns):
        """
        WAL/SHM sidecar files only exist while a local process has the
        DB open — copying them in mid-write would leave an inconsistent
        database inside the image.
        """
        assert self._is_excluded("data/sensors.db-wal", patterns)
        assert self._is_excluded("data/sensors.db-shm", patterns)

    def test_env_file_excluded(self, patterns):
        assert self._is_excluded(".env", patterns)


