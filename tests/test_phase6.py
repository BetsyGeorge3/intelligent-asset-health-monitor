class TestLambdaPackaging:
    def test_package_contains_handler_and_four_servers(self, tmp_path, monkeypatch):
        import importlib.util
        spec = importlib.util.spec_from_file_location("package_mod", ROOT / "aws" / "lambda" / "package.py")
        package_mod = importlib.util.module_from_spec(spec)
        spec.loader.exec_module(package_mod)

        # LAMBDA_DIR must stay pointing at the real aws/lambda/ folder,
        # since that's where handler.py (one of the files being
        # packaged) actually lives — only redirect the output path and
        # leave BUILD_DIR pointed at a guaranteed-absent directory so
        # this test exercises the no-dependencies warning path, the
        # same way TestEventBridgePackaging does.
        monkeypatch.setattr(package_mod, "BUILD_DIR", tmp_path / "_build_never_created")

        out_path = package_mod.build_package(output_path=tmp_path / "package.zip")

        with zipfile.ZipFile(out_path) as zf:
            names = zf.namelist()

        assert "handler.py" in names
        for server in ["cmms_mcp.py", "inventory_mcp.py", "scheduling_mcp.py", "notify_mcp.py", "schemas.py", "__init__.py"]:
            assert f"mcp_servers/{server}" in names
        # sensor_mcp.py deliberately excluded — needs torch, not a Lambda fit
        assert "mcp_servers/sensor_mcp.py" not in names


