"""
Phase 6 tests — AWS deployment artifacts.

What's tested here, and why these specific things:

1. .dockerignore correctness — a regression test for the real bug
   caught during development: a bare `*.db` pattern would have
   silently excluded data/sensors.db (needed at build time by
   Dockerfile.sensor) while only INTENDING to exclude the MCP servers'
   runtime-generated databases.

2. Dockerfile COPY paths actually exist in the repo.

3. mcp_servers/*.py DB/log paths are correctly overridable via env var
   — this is what lets docker-compose mount a volume at /app/data
   instead of shadowing /app/mcp_servers (the second real bug caught
   during development).

4. aws/lambda/handler.py correctly wraps each of the 4 lightweight
   FastAPI apps via Mangum, including the explicit init_db() call this
   project needs since lifespan="off" means FastAPI's lifespan context
   manager (which would otherwise create the SQLite tables) never runs.

5. aws/lambda/package.py and aws/eventbridge/deploy.py's packaging
   logic produce the expected file sets — verified by actually running
   the packaging functions and inspecting the resulting zip contents,
   not just reading the code.

6. aws/eventbridge/scheduler_handler.py's pure-logic helpers
   (_persist_trace, _extract_severity) work correctly in both local
   and S3 modes (S3 calls are mocked — no real AWS needed).

None of these tests require Docker, real AWS credentials, or live MCP
servers — they validate the deployment ARTIFACTS and PACKAGING LOGIC,
which is exactly what's reliably testable without a real cloud account.

Run:
    cd asset_health_monitor
    pytest tests/test_phase6.py -v
"""

