class TestSchedulerHandlerHelpers:
    def test_extract_severity_finds_anomaly_score_step(self):
        from aws.eventbridge.scheduler_handler import _extract_severity
        trace = {"steps": [
            {"tool_name": "get_anomaly_score", "tool_output": {"severity": "critical"}},
        ]}
        assert _extract_severity(trace) == "critical"

    def test_extract_severity_returns_none_when_absent(self):
        from aws.eventbridge.scheduler_handler import _extract_severity
        trace = {"steps": [{"tool_name": "create_work_order", "tool_output": {"status": "open"}}]}
        assert _extract_severity(trace) is None

    def test_persist_trace_local_mode(self, tmp_path, monkeypatch):
        monkeypatch.delenv("TRACE_BUCKET", raising=False)
        import importlib
        import aws.eventbridge.scheduler_handler as mod
        importlib.reload(mod)

        # Redirect the local-mode output dir into tmp_path
        monkeypatch.setattr(
            "aws.eventbridge.scheduler_handler.Path",
            mod.Path,  # no-op patch placeholder; real redirect below
        )
        # Easiest robust approach: monkeypatch __file__'s parent chain by
        # calling the function and checking the real default location,
        # then cleaning up — avoids over-mocking Path itself.
        trace = {"machine_id": "TEST-MACHINE", "steps": [], "n_steps": 0}
        mod._persist_trace(trace)

        expected = ROOT / "agent" / "traces" / "TEST-MACHINE.json"
        assert expected.exists()
        with open(expected) as f:
            assert json.load(f)["machine_id"] == "TEST-MACHINE"
        expected.unlink()  # cleanup — don't leave test residue in agent/traces/

    def test_persist_trace_s3_mode(self, monkeypatch):
        import unittest.mock as mock
        monkeypatch.setenv("TRACE_BUCKET", "fake-test-bucket")
        import importlib
        import aws.eventbridge.scheduler_handler as mod
        importlib.reload(mod)

        with mock.patch("boto3.client") as mock_boto:
            mock_s3 = mock.MagicMock()
            mock_boto.return_value = mock_s3

            trace = {"machine_id": "MOTOR-01", "steps": [], "n_steps": 0}
            mod._persist_trace(trace)

            assert mock_s3.put_object.called
            kwargs = mock_s3.put_object.call_args.kwargs
            assert kwargs["Bucket"] == "fake-test-bucket"
            assert kwargs["Key"] == "traces/MOTOR-01.json"
            assert kwargs["ContentType"] == "application/json"
